package com.stylefeng.guns.core.base.tips;

/**
 * 返回给前台的成功提示
 *
 * @author fengshuonan
 * @date 2016年11月12日 下午5:05:22
 */
public class SuccessTip extends Tip {
	
	private Object obj;
	
	public SuccessTip(){
		super.code = 200;
		super.message = "操作成功";
	}

	public Object getObj() {
		return obj;
	}

	public void setObj(Object obj) {
		this.obj = obj;
	}

	public SuccessTip(Object obj) {
		super.code = 200;
		super.message = "操作成功";
		this.obj = obj;
	}
	
	
}
